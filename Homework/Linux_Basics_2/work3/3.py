import sys

def decrypt(encrypted: str) -> str:
    """
    Расшифровывает сообщение по правилам:
    - точка '.' удаляет саму себя
    - две точки '..' удаляют предыдущий символ и себя
    - остальные символы добавляются в результат
    """
    result = []  # создаём пустой список (это будет наш стек)

    i = 0  # счётчик для прохода по строке
    length = len(encrypted)  # длина зашифрованной строки

    while i < length:  # пока не дошли до конца строки
        if encrypted[i] == '.':  # если текущий символ - точка
            # проверяем, есть ли следующая точка (две точки подряд)
            if i + 1 < length and encrypted[i + 1] == '.':
                # ДВЕ точки: удаляем последний символ из результата
                if result:  # если в результате есть символы
                    result.pop()  # удаляем последний
                i += 2  # пропускаем обе точки
            else:
                # ОДНА точка: просто пропускаем её
                i += 1
        else:
            # Обычный символ (буква, пробел, цифра, дефис) - добавляем в результат
            result.append(encrypted[i])
            i += 1

    return ''.join(result)  # превращаем список в строку


def main():
    """Основная функция - читает из pipe и выводит результат"""
    # Читаем ВСЁ, что пришло из pipe
    encrypted_message = sys.stdin.read()

    # Убираем символ переноса строки в конце (если есть)
    encrypted_message = encrypted_message.rstrip('\n')

    # Расшифровываем
    decrypted_message = decrypt(encrypted_message)

    # Выводим результат
    print(decrypted_message)


# Это точка входа - программа начинает работу отсюда
if __name__ == "__main__":
    # Просто тестируем функцию напрямую
    print(decrypt("абра-кадабра."))      # абра-кадабра
    print(decrypt("абраа..-кадабра"))    # абра-кадабра
    print(decrypt("1..2.3"))             # 23
    print(decrypt("абра........"))       # (пусто)